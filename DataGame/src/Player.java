
public class Player {
	private int score;
	private int curSpace;
	private String[] arr;
	
	public Player() {
		score = 0;
	}

	public String[] getArr() {
		return arr;
	}

	public void setArr(String[] arr) {
		this.arr = arr;
	}

	public int getScore() {
		return score;
	}

	public void setScore(int score) {
		this.score = score;
	}

	public int getCurSpace() {
		return curSpace;
	}

	public void setCurSpace(int curSpace) {
		this.curSpace = curSpace;
	}
	
	
}
